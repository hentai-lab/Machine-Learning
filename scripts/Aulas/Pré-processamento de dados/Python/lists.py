# LISTS
# Um conjunto de valores que pode serheterogêneo.

pesosV = [7.76, 80.3, 90, 117.4] # Cria um vetor dinâmico com tamanho variado
nomesV = ["Maria", "Carlos", "Pedro", "Antônio"] # Cria um vetor dinâmico com tamanho variado

myList = [pesosV, nomesV] # Cria um vetor dinâmico com tamanho variado com dois vetores como valores
print(myList) # Printa os valores do vetor no console
print(myList[0]) # Printa apenas os valores do primeiro vetor (pesosV) que está dentro do vetor myList
print(myList[1]) # Printa apenas os valores do segundo vetor (nomesV) que está dentro do vetor myList